# 家
houseArray = Array.new
CSV.foreach('sampledata/convertedHousesCSV.csv') do |row|
  architectID   = row[0]
  architectName = row[1]
  houseNo       = row[2]
  houseID       = row[3]
  name          = row[4]
  cost          = row[5]
  area          = row[6]
  space         = row[7]
  description   = row[8]
  suvacoURL     = row[9]
    
  house = House.create(
  :uuid=>houseID,
  :view_count=>341,
  :title=>name,
  :description=>description,
  :cost=>cost,
  :area=>area,
  :space=>space)

  # relation
  architectArray.each do |architect|
    if architect.uuid == architectID
      #puts("architectみつけた")
      architect.houses << house
      break
    end
  end
  
  houseArray.push(house)
end

